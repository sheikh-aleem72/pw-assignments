// Example of IF statement.
let x = 10;

if (x > 5) {
  console.log("x is greater than 5");
}

// Example of IF-ELSE statement.
let temperature = 25;

if (temperature > 30) {
  console.log("It's hot outside");
} else {
  console.log("It's not too hot");
}

// Example of ELSE-IF statement.
let grade = 75;

if (grade >= 90) {
  console.log("A");
} else if (grade >= 80) {
  console.log("B");
} else if (grade >= 70) {
  console.log("C");
} else {
  console.log("Fail");
}
